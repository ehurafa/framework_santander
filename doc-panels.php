    <h2 class="doc-title" id="link-doc-panels">
      Panels
    </h2>

    <div class="legend">
      <p>
        Os painéis são as estruturas básicas para compor o conteúdo de uma área. <br><br>
        São espaços que podem delimitar cada seção e deixar mais harmônico as informações expostas.<br><br>
        Pode ser aplicado de forma fixa e com acordeões internos para separar a navegação dentro do painel.<br><br>
        Botões de ação devem estar localizados na barra superior ou inferior do painel.<br><br>

      </p>
