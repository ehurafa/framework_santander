<h2 class="doc-title" id="link-doc-botao">
Botões
</h2>

<div class="legend">
   <p>Os botões utilizam as mesmas propriedades definidas como fonte e um outline para marcar a borda do botão. No hover o botão é preenchido. Utilizamos este recurso para deixar os botões mais leves e evitar muitos elementos vermelhos simultâneos na tela.</p><br>
   <p>Em alguns casos serão necessários outros botões de apoio e ações na mesma tela. Então é permitido a utilização de cores de apoio para equilibrar o visual e ações na tela.</p>
</div>

